def render_section(lead: str, terms: list[tuple[str, str, str, str]]) -> str:
    lines = [
        BEGIN,
        "## 读之前先把这篇的名词说清楚",
        "",
        lead,
        "",
        "后面如果你看到这些词，先不要急着背定义。你可以按下面这个顺序理解：",
        "",
        "```text",
        "它是什么 -> 在这个例子里负责什么 -> 面试时怎么说",
        "```",
        "",
    ]
    for index, (name, novice, scene, interview) in enumerate(terms, start=1):
        lines.extend(
            [
                f"### {index}. {name}",
                "",
                f"**新手理解**：{novice}",
                "",
                f"**在这个例子里**：{scene}",
                "",
                f"**面试说法**：{interview}",
                "",
            ]
        )
    lines.append(END)
    lines.append("")
    return "\n".join(lines)


def update_file(path: Path, config: dict[str, object]) -> bool:
    text = path.read_text(encoding="utf-8")
    text = re.sub(
        rf"\n?{re.escape(BEGIN)}.*?{re.escape(END)}\n*",
        "\n",
        text,
        flags=re.S,
    )
    section = render_section(config["lead"], config["terms"])  # type: ignore[arg-type]
    marker = "\n## 0. 总流程图"
    if marker not in text:
        raise ValueError(f"Cannot find flowchart marker in {path}")
    new_text = text.replace(marker, f"\n{section}\n## 0. 总流程图", 1)
    if new_text != text:
        path.write_text(new_text, encoding="utf-8", newline="\n")
        return True
    return False


def main() -> None:
    important_files = sorted(ROOT.rglob("！重要！一个例子串起来*.md"))
    by_name = {path.name: path for path in important_files}

    missing_configs = sorted(set(by_name) - set(SECTIONS))
    missing_files = sorted(set(SECTIONS) - set(by_name))
    if missing_configs or missing_files:
        raise SystemExit(
            "Configuration mismatch:\n"
            f"  files without config: {missing_configs}\n"
            f"  configs without file: {missing_files}"
        )

    changed = []
    for name, config in SECTIONS.items():
        path = by_name[name]
        if update_file(path, config):
            changed.append(path)

    print(f"important files checked: {len(important_files)}")
    print(f"files changed: {len(changed)}")
    for path in changed:
        print(path.relative_to(ROOT))


if __name__ == "__main__":
    main()
